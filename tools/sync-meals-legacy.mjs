import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';

const LEGACY_SOURCE_PATH = path.resolve(process.cwd(), 'legacy/refeicoes.html');
const GENERATED_OUTPUT_PATH = path.resolve(
  process.cwd(),
  'src/components/screens/dashboard/sections/presentation/mealsLegacy.generated.ts',
);

const readLegacyMeals = async () => {
  const html = await readFile(LEGACY_SOURCE_PATH, 'utf8');

  const bodyMatch = html.match(/<body[^>]*>([\s\S]*?)<\/body>/i);
  if (!bodyMatch) {
    throw new Error('Could not find <body> in legacy/refeicoes.html');
  }

  const scriptMatch = bodyMatch[1].match(/<script[^>]*>([\s\S]*?)<\/script>/i);
  if (!scriptMatch) {
    throw new Error('Could not find <script> in legacy/refeicoes.html body');
  }

  const script = scriptMatch[1].trim();
  const htmlWithoutScripts = bodyMatch[1].replace(/<script[\s\S]*?<\/script>/gi, '').trim();

  return { html: htmlWithoutScripts, script };
};

const escapeForTemplateLiteral = (value) => {
  return value.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
};

const buildGeneratedFile = ({ html, script }) => {
  const nowIso = new Date().toISOString();
  const escapedHtml = escapeForTemplateLiteral(html);
  const escapedScript = escapeForTemplateLiteral(script);

  return `/* eslint-disable */
/**
 * Auto-generated by tools/sync-meals-legacy.mjs
 * Source: legacy/refeicoes.html
 * Generated at: ${nowIso}
 */

export const LEGACY_MEALS_HTML = \`${escapedHtml}\`;

export const LEGACY_MEALS_SCRIPT = \`${escapedScript}\`;
`;
};

const run = async () => {
  const meals = await readLegacyMeals();
  const generatedContent = buildGeneratedFile(meals);

  await mkdir(path.dirname(GENERATED_OUTPUT_PATH), { recursive: true });
  await writeFile(GENERATED_OUTPUT_PATH, generatedContent, 'utf8');

  process.stdout.write(`Synced meals legacy content -> ${GENERATED_OUTPUT_PATH}\n`);
};

run().catch((error) => {
  process.stderr.write(`sync-meals-legacy failed: ${error instanceof Error ? error.message : String(error)}\n`);
  process.exitCode = 1;
});
